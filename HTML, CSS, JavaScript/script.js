const quiz = document.getElementById('quiz')
const answerEls = document.querySelectorAll('.answer')
const questionEl = document.getElementById('question')
const a_text = document.getElementById('a_text')
const b_text = document.getElementById('b_text')
const c_text = document.getElementById('c_text')
const d_text = document.getElementById('d_text')
const submitBtn = document.getElementById('submit')

let currentQuiz = 0
let score = 0

loadQuiz()

function loadQuiz() {
    deselectAnswers()
    if(location. href.split("/").pop() == "questionsIpl.html"){
        const currentQuizData = quizDataIpl[currentQuiz]
        questionEl.innerText = currentQuizData.question
        a_text.innerText = currentQuizData.a
        b_text.innerText = currentQuizData.b
        c_text.innerText = currentQuizData.c
        d_text.innerText = currentQuizData.d
    }
    else if(location. href.split("/").pop() == "questionsEntertainment.html"){
        const currentQuizData = quizDataEntertainment[currentQuiz]
        questionEl.innerText = currentQuizData.question
        a_text.innerText = currentQuizData.a
        b_text.innerText = currentQuizData.b
        c_text.innerText = currentQuizData.c
        d_text.innerText = currentQuizData.d
    }
    else if(location. href.split("/").pop() == "questionsCurrentAffairs.html"){
        const currentQuizData = quizDataCurrentAffair[currentQuiz]
        questionEl.innerText = currentQuizData.question
        a_text.innerText = currentQuizData.a
        b_text.innerText = currentQuizData.b
        c_text.innerText = currentQuizData.c
        d_text.innerText = currentQuizData.d
    }
}

function deselectAnswers() {
    answerEls.forEach(answerEl => answerEl.checked = false)
}

function getSelected() {
    let answer

    answerEls.forEach(answerEl => {
        if(answerEl.checked) {
            answer = answerEl.id
        }
    })

    return answer
}

submitBtn.addEventListener('click', () => {
    const answer = getSelected()
    if(answer) {
        if(location. href.split("/").pop() == "questionsIpl.html"){
            if(answer === quizDataIpl[currentQuiz].correct) {
                score+= 4
            } else {
                score-= 1
            }
        }
        if(location. href.split("/").pop() == "questionsEntertainment.html"){
            if(answer === quizDataEntertainment[currentQuiz].correct) {
                score+= 4
            } else {
                score-= 1
            }
        }
        if(location. href.split("/").pop() == "questionsCurrentAffairs.html"){
            if(answer === quizDataCurrentAffair[currentQuiz].correct) {
                score+= 4
            } else {
                score-= 1
            }
        }

        currentQuiz++

        if(currentQuiz < quizDataIpl.length) {
            loadQuiz()
        } else{
            quiz.innerHTML = `
                <h2>You scored ${score}/${quizDataIpl.length * 4}</h2>
                <br>
                <center>
                <button id="submit" onclick="button()">Solve other quizzes?</button></center>
            `
        }
    }
})

function button() {
    location.href = "quiz.html";
} 
